    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        DefaultTableModel modal = (DefaultTableModel) jTable1.getModel();
        int row =jTable1.getSelectedRow();
        txtLabourID.setText(modal.getValueAt(row, 0).toString());
        txtName.setText(modal.getValueAt(row, 1).toString());
        txtAddress.setText(modal.getValueAt(row, 4).toString());
        txtNic.setText(modal.getValueAt(row, 3).toString());
        txtTp.setText(modal.getValueAt(row, 5).toString());
        String sex = modal.getValueAt(row, 2).toString();
        if(sex.equals("Male"))
        {
            radioMale.setSelected(true);
            radioFemale.setSelected(false);
            radioOther.setSelected(false);
        }
        else if(sex.equals("Female"))
        {
            radioFemale.setSelected(true);
             radioMale.setSelected(false);
            radioOther.setSelected(false);
        }
        else
        {
            radioOther.setSelected(true);
             radioMale.setSelected(false);
            radioFemale.setSelected(false);
        }
                


        
    }//GEN-LAST:event_jTable1MouseClicked

