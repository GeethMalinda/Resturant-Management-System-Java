    private String getSelectedRadioButton() {
       
        if(radioMale.isSelected())
        {
            return "Male";
        }
        else if(radioFemale.isSelected())
        {
            return "Female";
        }
        else
        {
          return "Other";
        
        }
    }

    private void getLabourListtoTable() throws ClassNotFoundException, SQLException {

        DefaultTableModel modal = (DefaultTableModel) jTable1.getModel();
        modal.setRowCount(0);
        List<Labour>list  = new LabourController().getLabourList();

        for(Labour tempList : list)
        {
          modal.addRow(new Object[]{
              tempList.getEmpId(),
              tempList.getName(),
              tempList.getSex(),
              tempList.getNic(),
              tempList.getAdress(),
              tempList.getTp()
              
          });
              
          
                  
                  
                  
        }
        


    }
    
