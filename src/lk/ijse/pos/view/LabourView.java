    private void txtLabourIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLabourIDActionPerformed

        if(txtLabourID.getText().trim().length()>0)
        {
            try {
                Labour obj = new LabourController().getLabourObj(txtLabourID.getText().trim());
                if(obj != null)
                {
                    txtLabourID.setText(obj.getEmpId());
                    txtName.setText(obj.getName());
                    txtAddress.setText(obj.getAdress());
                    txtNic.setText(obj.getNic());
                    txtTp.setText(obj.getTp());
                    String sex = obj.getSex(); 
                    if(sex.equals("Male"))
                    {
                     radioMale.setSelected(true);
                     radioFemale.setSelected(false);
                     radioOther.setSelected(false);
                    }
                    else if(sex.equals("Female"))
                    {
                     radioFemale.setSelected(true);
                     radioMale.setSelected(false);
                     radioOther.setSelected(false);
                    }
                    else
                    {
                     radioOther.setSelected(true);
                     radioMale.setSelected(false);
                     radioFemale.setSelected(false);
                    }
                    
                   
                }
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(LabourView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(LabourView.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please Insert An Id","Error",JOptionPane.ERROR_MESSAGE);
        }
        
          
    }//GEN-LAST:event_txtLabourIDActionPerformed

