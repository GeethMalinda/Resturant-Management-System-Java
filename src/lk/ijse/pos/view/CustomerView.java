    private void loadToTable() throws ClassNotFoundException, SQLException {

        DefaultTableModel modal =(DefaultTableModel) tbl.getModel();
        modal.setRowCount(0);
        
        List<Customer>customerlist = new CustomerController().getCustomerList();
        for(Customer temp: customerlist)
        {
            modal.addRow(new Object[]{
                temp.getCustomerID(),
                temp.getName(),
                temp.getType()
                
            });
        }

    }
