    private void txtCustomerIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCustomerIDActionPerformed

        if(txtCustomerID.getText().trim().length()>0)
        {
            try {
                Customer obj = new CustomerController().getCustomerObj(txtCustomerID.getText().trim());
                if(obj != null)
                {
                    txtCustomerName.setText(obj.getName());
                    cmbCustomerType.setSelectedItem(obj.getType());
                }
                 else
                {
                    JOptionPane.showMessageDialog(null, "Try Again","Warning",JOptionPane.WARNING_MESSAGE);
                }
                
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "please insert id","Error",JOptionPane.ERROR_MESSAGE);
        }





        // TODO add your handling code here:
    }//GEN-LAST:event_txtCustomerIDActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        
        String id = txtCustomerID.getText();
        String name = txtCustomerName.getText();
        String type = cmbCustomerType.getSelectedItem().toString();
        Customer obj = new Customer(id, name, type);
        if(txtCustomerID.getText().trim().length()>0)
        {
            try {
                boolean isSave= new CustomerController().saveCustomer(obj);
               
                if(isSave)
                {
                    JOptionPane.showMessageDialog(this, "Saved");
                    loadToTable();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Try Again","Warning",JOptionPane.WARNING_MESSAGE);
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "please Insert an Id","Error",JOptionPane.ERROR_MESSAGE);
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed
 
